package Week3;

public class Increase {

    public static void main(String[] args) {

        // ++ 연산자
        int a = 10;
        System.out.println("기본 변수값 : " + a);
        a++;
        System.out.println("++ 한 이후의 변수값 : " + a);

        // -- 연산자
        int b = 20;
        System.out.println("기본 변수값 : " + b);
        b--;
        System.out.println("-- 한 이후의 변수값 : " + b);


    }
}